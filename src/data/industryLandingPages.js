// Splits "X That Y" headlines into a plain lead-in + gradient-highlighted
// close, matching the H1 treatment on /cleaningservices. Verticals whose
// headline doesn't contain " That " pass an explicit splitOn override.
function splitHeadline(h1, splitOn = ' That ') {
  const idx = h1.indexOf(splitOn)
  if (idx === -1) return { pre: h1, highlight: '' }
  return { pre: h1.slice(0, idx + splitOn.length), highlight: h1.slice(idx + splitOn.length) }
}

